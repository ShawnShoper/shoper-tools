package org.shoper.dynamiccompile;

import java.net.URLClassLoader;
import java.util.concurrent.atomic.AtomicInteger;
import java.util.concurrent.atomic.AtomicLong;

public class ClassLoaderHoloder
{
	private URLClassLoader classLoader;
	private AtomicInteger holderCount;
	private AtomicLong lastHouderTime;
	public URLClassLoader getClassLoader()
	{
		return classLoader;
	}
	public void setClassLoader(URLClassLoader classLoader)
	{
		this.classLoader = classLoader;
	}
	public AtomicInteger getHolderCount()
	{
		return holderCount;
	}
	public AtomicLong getLastHouderTime()
	{
		return lastHouderTime;
	}

}
